# Crie um algoritimo que leia um numero, e mostre  o seu dobro, o triplo e a raiz quadrada.

Number_ONE = int(input('Insira um valor númerico'))

dobro = Number_ONE*2
Triplo = Number_ONE*3
Raiz = Number_ONE**(1/2)

print(f'O dobro de {Number_ONE} é {dobro}, e o triplo é{Triplo}, e a Raiz é de {Raiz:.2f}')